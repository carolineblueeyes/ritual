import { RitualScript, ScriptSegment, GroupName, HapticSpec, MusicLayer, VoiceInstruction } from "../types";

// ===== Вспомогательные функции =====

function seg(
  phase: ScriptSegment['phase'],
  from: number, to: number,
  voice: VoiceInstruction | null,
  music?: Partial<MusicLayer>,
  haptic?: Partial<HapticSpec>,
): ScriptSegment {
  return {
    phase, from, to,
    ...(voice ? { voice } : {}),
    ...(music ? { music: { padFreq: 40, instrument: 'тёплый пэд', volume: 0.3, ...music } } : {}),
    ...(haptic ? { haptic: { pattern: 'pulse', intensity: 0.3, ...haptic } } : {}),
  };
}

function parseDur(d: string): number {
  const p = d.split(':');
  return parseInt(p[0]) * 60 + parseInt(p[1]);
}

