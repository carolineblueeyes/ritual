const HAPTICS: Record<string, Partial<HapticSpec>> = {
  calm:    { pattern: 'pulse',      intensity: 0.2 },
  focus:   { pattern: 'continuous', intensity: 0.15 },
  tense:   { pattern: 'wave',       intensity: 0.6 },
  release: { pattern: 'wave',       intensity: 0.3 },
  chime:   { pattern: 'chime',      intensity: 0.5 },
  silent:  { pattern: 'none',       intensity: 0 },
};

function groupDefaults(g: GroupName) {
  return GROUP_DEFAULTS[g];
}

// ===== ПОГРУППОВЫЕ СКРИПТЫ РИТУАЛОВ =====

function buildRitualScript(
  ritualId: string, group: GroupName, title: string,
  duration: string, category: string,
  scientificBase: string, howItWorks: string,
  segments: ScriptSegment[],
  expectedImpacts?: RitualScript['expectedImpacts'],
  postLinks?: RitualScript['postLinks'],
): RitualScript {
  const gd = groupDefaults(group);
  return {
    ritualId, group, title,
    durationSeconds: parseDur(duration),
    category, scientificBase, howItWorks,
    segments: segments.map(s => ({
      ...s,
      music: s.music ? { padFreq: gd.baseFreq, instrument: gd.baseInstrument, volume: gd.baseVolume, ...s.music } : undefined,
      haptic: s.haptic ? { pattern: 'pulse', intensity: 0.3, ...s.haptic } : undefined,
    })),
    expectedImpacts: expectedImpacts || [],
    postLinks: postLinks || [],
  };
}

